def classify(seller_name: str = "", industries: str = "",
             custom_rules: Optional[list[dict]] = None) -> Optional[dict]:
    """判斷會計科目。

    custom_rules: 使用者自訂規則 list，每筆 `{keywords: [str], subject: str,
        match: 'industries' | 'name' | 'any'}`。優先級**高於**內建規則。

    Returns dict `{name: str, source: 'custom-industries' | 'custom-name'
        | 'industries' | 'name'}` 或 None。
    """
    industries = (industries or "").strip()
    name = (seller_name or "").strip()

    # Priority 0: 自訂規則（使用者明確指定，最高優先）
    if custom_rules:
        for rule in custom_rules:
            if not isinstance(rule, dict):
                continue
            kws = rule.get("keywords") or []
            subject = rule.get("subject") or ""
            match = rule.get("match") or "any"
            if not subject or not isinstance(kws, list):
                continue
            for kw in kws:
                if not isinstance(kw, str) or not kw.strip():
                    continue
                kw = kw.strip()
                # match='industries' / 'name' / 'any'
                if match in ("any", "industries") and industries and kw in industries:
                    return {"name": subject, "source": "custom-industries"}
                if match in ("any", "name") and name and kw in name:
                    return {"name": subject, "source": "custom-name"}

    # Priority 1: 內建 industries 規則（高信心）
    if industries:
        for keywords, subject in _INDUSTRY_RULES:
            for kw in keywords:
                if kw in industries:
                    return {"name": subject, "source": "industries"}

    # Priority 2: 內建 name 規則（中信心；用具體多字 keyword 避免誤判）
    if name:
        for keywords, subject in _NAME_RULES:
            for kw in keywords:
                if kw in name:
                    return {"name": subject, "source": "name"}

    return None


# 給 UI 用：取得內建規則的精簡版（readonly 顯示）
def get_builtin_rules() -> list[dict]:
    return [
        {"keywords": kws, "subject": sub, "match": "industries"}
        for kws, sub in _INDUSTRY_RULES
    ] + [
        {"keywords": kws, "subject": sub, "match": "name"}
        for kws, sub in _NAME_RULES
    ]


# 給 UI / tests 用
ALL_SUBJECTS: list[str] = sorted({sub for _, sub in _INDUSTRY_RULES}
                                  | {sub for _, sub in _NAME_RULES})
